namespace NKnife.Storages
{
    /// <summary>
    ///     与面向数据库的SQL的语言中的Order的排序模式对应
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// 默认不排序
        /// </summary>
        NONE,
        /// <summary>
        /// 降序
        /// </summary>
        DESC,
        /// <summary>
        /// 升序
        /// </summary>
        ASC
    }
}